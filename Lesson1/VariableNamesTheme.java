package Lesson1;

public class VariableNamesTheme {
    public static void main(String[] args) {
        System.out.println("1. Разные переменные");
        // Цифра
        int digit = 3;
        System.out.println("Цифра = " + digit);

        // Cумма чисел
        int amountNumbers = 7;
        System.out.println("Cумма чисел = " + amountNumbers);

        // Произведение цифр
        int productDigits = 78;
        System.out.println("Произведение цифр = " + productDigits);

        // Максимальное число
        int maxNumber = 101;
        System.out.println("Максимальное число = " + maxNumber);

        // Количество десятков
        int quantityTens = 7;
        System.out.println("Количество десятков = " + quantityTens);

        // Вес собаки
        int weightDogs = 30;
        System.out.println("Вес собаки = " + weightDogs);

        // Исходное число
        int originalNumber = 56;
        System.out.println("Исходное число = " + originalNumber);

        // Процент по вкладу
        int percentDeposit = 15;
        System.out.println("Процент по вкладу = " + percentDeposit);

        // Переменная хранит символ &
        char symbol = '&';
        System.out.println("Переменная хранит символ = " + symbol);

        // Код ошибки
        String errorCode = "0xc00000e9";
        System.out.println("Код ошибки : " + errorCode);

        // Тип сообщения
        String typeMassage = "POST";
        System.out.println("Тип сообщения : " + typeMassage);

        // Число нулей
        int numberZeros = 7;
        System.out.println("Число нулей = " + numberZeros);

        // Уникальное число
        int uniqueNumber = 9;
        System.out.println("Уникальное число = " + uniqueNumber);

        // Случайное число
        int randomNumber = (int) (Math.random() * 100);
        System.out.println("Случайное число = " + randomNumber);

        // Математическое выражение
        int a = 7;
        int b = 9;
        int mathExpression = a + b;
        System.out.println("Математическое выражение = " + mathExpression);

        // Счет в игре
        int gameScore = 1111;
        System.out.println("Счет в игре = " + gameScore);

        // Максимальная длина
        int maxLength = 10;
        System.out.println("Максимальная длина = " + maxLength);

        // Максимальная длина
        int menuItem = 5;
        System.out.println("Максимальная длина = " + menuItem);

        // Стоимость кофе
        int coffeeCost = 78;
        System.out.println("Стоимость кофе = " + coffeeCost);

        // Дата начала
        String startDate = "01.10.2024";
        System.out.println("Дата начала = " + startDate);

        // Окончание диапазона
        int endRange = 6;
        System.out.println("Окончание диапазона = " + endRange);

        // Полное имя работника месяца
        String fullNameEmployeeMonth = "Sergey Sergeev";
        System.out.println("Полное имя работника месяца = " + fullNameEmployeeMonth);

        // Заголовок электронной книги
        String ebookTitle = "eBookTitle";
        System.out.println("Заголовок электронной книги - " + ebookTitle);

        // Размер
        int size = 5;
        System.out.println("Размер = " + size);

        // Вместимость
        int capacity = 67;
        System.out.println("Вместимость = " + capacity);

        // Cчетчик
        int counter = 70;
        System.out.println("Cчетчик = " + counter);

        // Путь до файла
        String pathToFile = "C:\\Users\\Sergey Sergeev\\Desktop\\";
        System.out.println("Путь до файла = " + pathToFile);

        // Количество чисел в каждой строке
        int quantityNumbersPerLine = 101;
        System.out.println("Количество чисел в каждой строке = " + quantityNumbersPerLine);

        System.out.println(" " + "\n" + "2. Boolean-переменные");
        // Сотни равны?
        boolean isEqualsHundreds = true;
        System.out.println("Сотни равны? = " + isEqualsHundreds);

        // Компьютер включен?
        boolean isComputerTurnedOn = false;
        System.out.println("Компьютер включен? = " + isComputerTurnedOn);

        // Есть равные цифры?
        boolean isEqualsDigits = true;
        System.out.println("Есть равные цифры? = " + isEqualsDigits);

        // Cоздано?
        boolean isCreated = true;
        System.out.println("Cоздано ? = " + isCreated);

        // Пустое?
        boolean isEmpty = true;
        System.out.println("Пустое ? = " + isEmpty);

        // Активное?
        boolean isActive = true;
        System.out.println("Активное ? = " + isActive);

        // Новое?
        boolean isNew = true;
        System.out.println("Новое? = " + isNew);

        // Электронная почта действительная?
        boolean isValidEmail = true;
        System.out.println("Электронная почта действительная? = " + isValidEmail);

        // Имеются уникальные строки?
        boolean isUniqueStrings = true;
        System.out.println("Имеются уникальные строки? = " + isUniqueStrings);

        System.out.println(" " + "\n" + "3. Аббревиатуры ");

        // Старый universally unique identifier
        int deprecatedUuid = 5;
        System.out.println("Старый universally unique identifier = " + deprecatedUuid);

        // Производитель оперативной памяти
        String manufacturerRam = "AMD";
        System.out.println("Производитель оперативной памяти = " + manufacturerRam);

        // Ёмкость жесткого диска
        int capacityHd = 50;
        System.out.println("Ёмкость жесткого диска = " + capacityHd);

        // Протокол передачи гипертекста
        String http = "HTTP";
        System.out.println("Протокол передачи гипертекста = " + http);

        // Сокращенный uniform resource locator
        String shortUrl = "URL";
        System.out.println("Сокращенный uniform resource locator = " + shortUrl);

        // American standard code for information interchange
        String ascii = "ASCII";
        System.out.println("American standard code for information interchange = " + ascii);
    }
}


